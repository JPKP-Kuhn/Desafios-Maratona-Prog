#include <bits/stdc++.h>
using namespace std;
using ll = long long;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  ll a, b, c; cin >> a >> b >> c;
  if (a != b && a != c) cout << "A\n";
  else if (b != a && b != c) cout << "B\n";
  else if (c != a && c != b) cout << "C\n";
  else cout << "*\n";

  return 0;
}

